package defaultPack;

public class Conteudo {
	
	private String titulo;
	private String urlImg;
	
	public Conteudo(String titulo, String urlImg) {
		this.titulo = titulo;
		this.urlImg = urlImg;
	}

	public String getTitulo() {
		return titulo;
	}

	public String getUrlImg() {
		return urlImg;
	}	
}
